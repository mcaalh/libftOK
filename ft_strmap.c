#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	int		i;
	char	*str;

	if (s == NULL || f == NULL)
		return (NULL);
	str = (char *)malloc(sizeof(str) * (ft_strlen(s) + 1));
	i = 0;
	if (str == NULL)
		return (NULL);
	while (s[i])
	{
		str[i] = f(s[i]);
		i++;
	}
	return (str);
}
